package com.example.cmput301w23t34;

public class Parallelogram extends Shape {

    private int l;
    private int w;
    private int angle;

    public Parallelogram(int x, int y, int l, int w, int angle) {
        this.x = x;
        this.y = y;
        this.l = l;
        this.w = w;
        this.angle = angle;
    }
}
